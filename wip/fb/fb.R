## Facebook performance metrics

This dataset is publicly available for research.

> [Moro et al., 2016](http://www.sciencedirect.com/science/article/pii/S0148296316000813): S. Moro, P. Rita and B. Vala. Predicting social media performance metrics and evaluation
of the impact on brand building: A data mining approach. Journal of Business Research, Elsevier, 2016. Available at: http://dx.doi.org/10.1016/j.jbusres.2016.02.010

4. Relevant Information:

  The data is related to posts' published during the year of 2014 on the Facebook's page of a renowned cosmetics brand.
This dataset contains 500 of the 790 rows and part of the features analyzed by Moro et al. (2016). The remaining were
omitted due to confidentiality issues.
